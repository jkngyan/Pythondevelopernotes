'''Variables:

Variables are used to store data that can be used and manipulated throughout your program.

In Python, you do not need to declare the type of a variable explicitly.
the interpreter infers the type from the value assigned to it.
This is the reason why python is called loosely typed programing language.
'''

x = 10  # x is an integer
y = 20.5  # y is a float
name = "Alice"  # name is a string
is_student = True  # is_student is a boolean

'''
Data Types:

1> Numeric Types:
int: Integer numbers (e.g., 10, -3)

2> float: Floating-point numbers (e.g., 10.5, -3.14)

3> String Type:
str: Sequence of characters (e.g., "Hello", 'Python')

4> Boolean Type:
bool: Represents True or False

Other Types:
list: Ordered collection of items (e.g., [1, 2, 3])
tuple: Immutable ordered collection of items (e.g., (1, 2, 3))
dict: Key-value pairs (e.g., {"name": "Alice", "age": 25})
set: Unordered collection of unique items (e.g., {1, 2, 3})
'''

# Example of integer variable
age = 25
print("Age:", age)

# Example of float variable
height = 5.9
print("Height:", height)

# Example of string variable
name = "Alice"
print("Name:", name)

# Example of boolean variable
is_student = True
print("Is Student:", is_student)

# Example of list variable
fruits = ["apple", "banana", "cherry"]
print("Fruits:", fruits)

# Example of tuple variable
coordinates = (10.0, 20.0)
print("Coordinates:", coordinates)

# Example of dictionary variable
person = {"name": "Alice", "age": 25}
print("Person:", person)

# Example of set variable
unique_numbers = {1, 2, 3, 4, 5}
print("Unique Numbers:", unique_numbers)
