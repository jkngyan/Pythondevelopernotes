'''
Input:

The input() function is used to take input from the user. It always returns the input as a string.
'''

name = input("Enter your name: ")
print("Hello, " + name)

'''
Output:

The print() function is used to output data to the console.
You can pass multiple arguments to print() separated by commas.

Formatted strings (f-strings) can be used to include variables within a string.
'''

age = 25
print("Age:", age)
height = 5.9
print(f"Height: {height}")


